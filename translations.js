// Language management
class LanguageManager {
    constructor() {
        this.currentLanguage = this.getStoredLanguage() || 'it';
        this.init();
    }
    
    init() {
        this.createLanguageSelector();
        this.translatePage();
    }
    
    getStoredLanguage() {
        return localStorage.getItem('selectedLanguage');
    }
    
    setStoredLanguage(lang) {
        localStorage.setItem('selectedLanguage', lang);
    }
    
    createLanguageSelector() {
        // Find existing navbar or create language selector
        const navbar = document.querySelector('#navbar-home, #navbar-photos, #navbar-quiz, nav');
        if (!navbar) return;
        
        // Check if language selector already exists
        if (document.getElementById('language-selector')) return;
        
        const languageSelector = document.createElement('div');
        languageSelector.id = 'language-selector';
        languageSelector.className = 'language-selector';
        
        // Create responsive options
        this.updateLanguageSelectorContent(languageSelector);
        
        // Append to navbar (will be positioned with CSS)
        navbar.appendChild(languageSelector);
        
        // Add event listener
        const selectElement = languageSelector.querySelector('#language-select');
        selectElement.addEventListener('change', (e) => {
            this.changeLanguage(e.target.value);
        });
        
        // Add resize listener for responsive behavior
        window.addEventListener('resize', () => {
            this.updateLanguageSelectorContent(languageSelector);
        });
    }
    
    updateLanguageSelectorContent(container) {
        const isMobile = window.innerWidth <= 768;
        const isVerySmall = window.innerWidth <= 480;
        
        let options = '';
        if (isVerySmall) {
            // Very small screens: only flags
            options = `
                <option value="it" ${this.currentLanguage === 'it' ? 'selected' : ''}>üáÆüáπ</option>
                <option value="en" ${this.currentLanguage === 'en' ? 'selected' : ''}>üá¨üáß</option>
                <option value="bg" ${this.currentLanguage === 'bg' ? 'selected' : ''}>üáßüá¨</option>
                <option value="ru" ${this.currentLanguage === 'ru' ? 'selected' : ''}>üá∑üá∫</option>
                <option value="fr" ${this.currentLanguage === 'fr' ? 'selected' : ''}>üá´üá∑</option>
            `;
        } else {
            // Larger screens: flags + language names
            options = `
                <option value="it" ${this.currentLanguage === 'it' ? 'selected' : ''}>üáÆüáπ Italiano</option>
                <option value="en" ${this.currentLanguage === 'en' ? 'selected' : ''}>üá¨üáß English</option>
                <option value="bg" ${this.currentLanguage === 'bg' ? 'selected' : ''}>üáßüá¨ –ë—ä–ª–≥–∞—Ä—Å–∫–∏</option>
                <option value="ru" ${this.currentLanguage === 'ru' ? 'selected' : ''}>üá∑üá∫ –†—É—Å—Å–∫–∏–π</option>
                <option value="fr" ${this.currentLanguage === 'fr' ? 'selected' : ''}>üá´üá∑ Fran√ßais</option>
            `;
        }
        
        container.innerHTML = `<select id="language-select" class="language-select">${options}</select>`;
        
        // Reattach event listener
        const selectElement = container.querySelector('#language-select');
        selectElement.addEventListener('change', (e) => {
            this.changeLanguage(e.target.value);
        });
    }
    
    changeLanguage(lang) {
        this.currentLanguage = lang;
        this.setStoredLanguage(lang);
        this.translatePage();
        
        // Refresh dynamic content if we're on different pages
        if (typeof showQuiz === 'function' && window.currentQuizIndex !== undefined) {
            // Refresh home quiz if it exists
            showQuiz(window.currentQuizIndex);
        }
        
        if (typeof showFullQuiz === 'function' && window.currentFullQuizIndex !== undefined) {
            // Refresh full quiz if it exists
            showFullQuiz(window.currentFullQuizIndex);
        }
    }
    
    translatePage() {
        const currentTranslations = translations[this.currentLanguage] || translations.it;
        
        // Translate elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (currentTranslations[key]) {
                if (element.tagName === 'INPUT' && element.type === 'button') {
                    element.value = currentTranslations[key];
                } else if (element.hasAttribute('placeholder')) {
                    element.placeholder = currentTranslations[key];
                } else {
                    element.textContent = currentTranslations[key];
                }
            }
        });
        
        // Update page title if needed
        const titleElement = document.querySelector('title');
        if (titleElement && titleElement.hasAttribute('data-translate')) {
            const key = titleElement.getAttribute('data-translate');
            if (currentTranslations[key]) {
                titleElement.textContent = currentTranslations[key];
            }
        }
    }
    
    translate(key) {
        const currentTranslations = translations[this.currentLanguage] || translations.it;
        return currentTranslations[key] || key;
    }
}

